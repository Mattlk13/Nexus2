# Route registration
def register_routes(db, get_current_user, require_admin):
    """Register CI/CD pipeline routes"""
    from fastapi import APIRouter, BackgroundTasks
    router = APIRouter(tags=["Autonomous CI/CD"])
    
    pipeline = AutonomousCICDPipeline(db)
    
    @router.get("/capabilities")
    async def get_capabilities():
        return pipeline.get_capabilities()
    
    @router.post("/start")
    async def start_pipeline(background_tasks: BackgroundTasks):
        """Start continuous CI/CD loop"""
        background_tasks.add_task(pipeline.run_cicd_loop)
        return {
            "success": True,
            "message": "CI/CD pipeline started"
        }
    
    @router.get("/status")
    async def get_status():
        """Get pipeline status"""
        return await pipeline.get_pipeline_status()
    
    return router

def init_hybrid(db):
    return AutonomousCICDPipeline(db)
