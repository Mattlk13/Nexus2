    async def _mark_deployed(self, integration: Dict):
        """Mark integration as deployed"""
        await self.db.auto_generated_hybrids.update_one(
            {"service_name": integration["service_name"]},
            {"$set": {"status": "deployed", "deployed_at": datetime.now(timezone.utc)}}
        )
    
    async def _mark_failed(self, integration: Dict, results: Dict):
        """Mark integration as failed"""
        await self.db.auto_generated_hybrids.update_one(
            {"service_name": integration["service_name"]},
            {"$set": {
                "status": "failed",
                "failed_at": datetime.now(timezone.utc),
                "error": results
            }}
        )
    
    async def get_pipeline_status(self) -> Dict:
        """Get CI/CD pipeline status"""
        recent_deployments = await self.db.cicd_deployments.find(
            {}, {"_id": 0}
        ).sort("deployed_at", -1).limit(20).to_list(20)
        
        failed = await self.db.auto_generated_hybrids.count_documents({"status": "failed"})
        pending = await self.db.auto_generated_hybrids.count_documents({"status": "generated"})
        deployed = await self.db.auto_generated_hybrids.count_documents({"status": "deployed"})
        
        success_rate = (deployed / (deployed + failed) * 100) if (deployed + failed) > 0 else 0
        
        return {
            "status": "active",
            "stats": {
                **self.pipeline_stats,
                "success_rate": success_rate,
                "pending": pending,
                "deployed": deployed,
                "failed": failed
            },
            "recent_deployments": recent_deployments
        }
    
